#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	unsigned int	i;

	if (!s)
		return (NULL);
	i = ft_strlen((char *)s);
	while (i > 0)
	{
		if (s[i] == (unsigned char)c)
			return ((char *)&s[i]);
		i--;
	}
	if (s[0] == (unsigned char)c)
		return ((char *)&s[0]);
	return (NULL);
}
