#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483647)
	{
		write(fd, "-2147483647", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd((n % 10) + '0', fd);
}

